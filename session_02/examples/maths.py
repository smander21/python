# Basic maths
print(1 + 9)
print(4 - 2)
print(3 * 3)
print(144 / 12)

# Using modulus to find the remainder
print(10 % 3)

# Storing the result in variables
x = 3
y = 7
area = x * y
print("The area of a rectangle '" + str(x) + " by " + str(y) + "' is: " + str(area))

# Take the users input for the radius and work out the area of the circle
PI = 3.14159
radius = float(input("Please enter the radius of a circle: "))
area = PI * (radius * radius)
print("The area of the circle with radius '" + str(radius) + "' is '" + str(area) + "'.")
