# A1 - Create two variables that hold the width and height of a rectangle,
# work out and store the area in a third variable.
# Print out the string: `Rectangle of width <x> and height <y> has an area of <area>`

# Put our width and height values into variables
width = 20
height = 12

# Calculate the area
area = width * height

# Print out the output - casting all int values to strings
print("Rectangle of width " + str(width) + " and height " + str(height) + " has an area of " + str(area))
